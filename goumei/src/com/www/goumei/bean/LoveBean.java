package com.www.goumei.bean;

/**
 * 喜欢实体类
 * 
 * @author Eric.Chen
 * 
 */
public class LoveBean {

	private int icon;
	private String name;
	private String time;
	private String text;
	private int img;

	public int getIcon() {
		return icon;
	}

	public void setIcon(int icon) {
		this.icon = icon;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public int getImg() {
		return img;
	}

	public void setImg(int img) {
		this.img = img;
	}

}
