package com.www.goumei.bean;

public class FansBean {
	private int fansIcon;
	private String fansName;
	private int fansSex;
	private int follow;

	public int getFansIcon() {
		return fansIcon;
	}

	public void setFansIcon(int fansIcon) {
		this.fansIcon = fansIcon;
	}

	public String getFansName() {
		return fansName;
	}

	public void setFansName(String fansName) {
		this.fansName = fansName;
	}

	public int getFansSex() {
		return fansSex;
	}

	public void setFansSex(int fansSex) {
		this.fansSex = fansSex;
	}

	public int getFollow() {
		return follow;
	}

	public void setFollow(int follow) {
		this.follow = follow;
	}

}
